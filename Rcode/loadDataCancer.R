ds <-  read.table(url(paste0("https://archive.ics.uci.edu/ml/machine-learning-databases/",
                                  "breast-cancer-wisconsin/wdbc.data")),header=FALSE,sep=",")

colnames(ds) <- c("id", "diagnosis", "radius_mean", "texture_mean", "perimeter_mean", "area_mean", "smoothness_mean", "compactness_mean", "concavity_mean",
                  "concave_points_mean", "symmetry_mean", "fractal_dimension_mean", "radius_se", "texture_se", "perimeter_se", "area_se", "smoothness_se",
                  "compactness_se", "concavity_se", "concave_points_se", "symmetry_se", "fractal_dimension_se", "radius_worst", "texture_worst", "perimeter_worst",
                  "area_worst", "smoothness_worst", "compactness_worst", "concavity_worst", "concave_points_worst", "symmetry_worst", "fractal_dimension_worst")
ds[ds$diagnosis == "B",]$diagnosis <- 1
ds[ds$diagnosis == "M",]$diagnosis <- 2

ds$diagnosis <- as.factor(ds$diagnosis)
#Drop ID
ds$id <- NULL

